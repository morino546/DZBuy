function normalizeText(text) {
  return (text || "").toLowerCase();
}

function detectCategoryFromText(text) {
  const value = normalizeText(text);
  if (!value) return "";
  if (value.includes("shoe") || value.includes("حذاء") || value.includes("sneaker")) return "shoes";
  if (value.includes("watch") || value.includes("ساعة")) return "watch";
  if (value.includes("phone") || value.includes("هاتف") || value.includes("redmi")) return "phone";
  if (value.includes("mixer") || value.includes("خلاط") || value.includes("kitchen")) return "kitchen";
  if (value.includes("earbud") || value.includes("سماعات") || value.includes("bluetooth")) return "audio";
  return "";
}

function categoryFromCard(card) {
  const title = normalizeText(card.dataset.title || "");
  return detectCategoryFromText(title);
}

function applySourceSelection(source) {
  selectedSource = source;
  sourceButtons.forEach((b) => b.classList.toggle("active", b.dataset.source === source));
}

function searchCheapestByCamera(fileNameHint) {
  clearCheapestHighlight();
  const category = detectCategoryFromText(fileNameHint);
  let targetCategory = category;
  if (!targetCategory) {
    const typed = prompt("لم أتعرف على المنتج من الصورة. اكتب اسم المنتج (مثال: حذاء، ساعة، هاتف):");
    targetCategory = detectCategoryFromText(typed || "");
  }

  if (!targetCategory) {
    setNotice("تعذر التعرف على نوع المنتج من الصورة. جرّب صورة أوضح أو اكتب اسم المنتج.", true);
    return;
  }

  const matches = productCards.filter((card) => categoryFromCard(card) === targetCategory);
  if (!matches.length) {
    setNotice("لا توجد منتجات مطابقة حاليًا لهذا النوع في Temu أو AliExpress.", true);
    return;
  }

  const cheapest = [...matches].sort((a, b) => Number(a.dataset.price || 0) - Number(b.dataset.price || 0))[0];
  const cheapestPrice = Number(cheapest.dataset.price || 0);
  const cheapestTitle = cheapest.dataset.title || "منتج";
  const cheapestSource = cheapest.dataset.source || "temu";

  applySourceSelection(cheapestSource);
  if (searchInput) {
    searchInput.value = "";
  }
  filterProducts("");

  cheapest.classList.add("cheapest-highlight");
  cheapestCardRef = cheapest;
  cheapest.scrollIntoView({ behavior: "smooth", block: "center" });

  const sourceLabel = cheapestSource === "temu" ? "Temu" : "AliExpress";
  setNotice(`تم البحث بالصورة في المصدرين. الأرخص: ${cheapestTitle} بسعر $${cheapestPrice.toFixed(2)} من ${sourceLabel}.`);
}

