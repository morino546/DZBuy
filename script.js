if (authModal) {
  authModal.addEventListener("click", (event) => {
    if (event.target === authModal) closeModal();
  });
}

if (emailForm) {
  emailForm.addEventListener("submit", (event) => {
    event.preventDefault();

    const name = document.getElementById("nameInput")?.value.trim() || "";
    const email = document.getElementById("emailInput")?.value.trim() || "";
    const phone = document.getElementById("emailPhoneInput")?.value.trim() || "";
    const password = document.getElementById("passwordInput")?.value || "";

    const emailValid = /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(email);
    if (!name || !emailValid || phone.replace(/\D/g, "").length < 8 || password.length < 6) {
      setStatus("تحقق من البيانات: الاسم، إيميل صحيح، هاتف، وكلمة مرور 6+", true);
      return;
    }

    saveLocalUser({ name, email, phone, method: "local" });
    if (accountLink) accountLink.textContent = name;
    setStatus("تم تسجيلك محليًا بنجاح.");
    setTimeout(closeModal, 700);
  });
}

(async () => {
  const authStatus = await fetchJson("/api/auth-status");
  setupSocialButtons(authStatus);

  const sessionData = await fetchJson("/api/me");
  const serverUser = sessionData?.user || null;
  const localUser = getLocalUser();

  if (accountLink) {
    if (serverUser) {
      accountLink.textContent = serverUser.displayName || "حسابي";
    } else if (localUser) {
      accountLink.textContent = localUser.name || "حسابي";
    }

    accountLink.addEventListener("click", async (event) => {
      const me = await fetchJson("/api/me");
      if (me?.user) return;

      if (!getLocalUser()) {
        event.preventDefault();
        openModal();
      }
    });
  }
})();
